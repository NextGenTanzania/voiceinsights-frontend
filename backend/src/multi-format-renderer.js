// FORMAT 2: MANAGEMENT REPORT (8-15 pages) — Programme Managers,
// Technical Leads, M&E Managers, Country Teams.
// ------------------------------------------------------------
export function buildManagementReportFormat(documentModel) {
  const dm = documentModel;
  const narrative = dm.narrative || {};
  const recs = dm.recommendations || {};
  const sectorIntelligence = buildSectorIntelligence(dm);

  const OWNER_BY_TIER = { immediate: 'Field/Operations Team', medium_term: 'Programme Management', long_term: 'Country Leadership / Donor Liaison' };
  const actionMatrix = [];
  for (const tier of ['immediate', 'medium_term', 'long_term']) {
    for (const action of (recs[tier] || [])) {
      actionMatrix.push({ action, tier, suggested_owner: OWNER_BY_TIER[tier] });
    }
  }

  return withPhase20FormatPackage({
    format: 'management_report',
    title: dm.metadata.template_name,
    prepared_for: dm.metadata.organization_name,
    campaign: dm.metadata.campaign_name,
    generated_at: dm.metadata.generated_at,
    is_demo: !!dm.is_demo,
    objectives: `Assess ${dm.metadata.template_name.toLowerCase()} conditions for ${dm.metadata.organization_name}, covering ${dm.kpis.regions_covered} regions with ${dm.kpis.total_responses} respondent contacts.`,
    methodology: {
      total_responses: dm.kpis.total_responses,
      response_rate_pct: dm.kpis.response_rate_pct,
      data_quality: dm.data_quality,
      standards: dm.metadata.standards,
    },
    charts: dm.charts || [],
    regional_comparison: dm.demographics?.regions || [],
    key_findings: narrative.key_findings || [],
    discussion: narrative.discussion || 'Insufficient verified evidence available for this section.',
    operational_risks: narrative.risks || [],
    opportunities: narrative.opportunities || [],
    recommendations: recs,
    action_matrix: actionMatrix,
    data_quality_assessment: dm.data_quality,
    sector_intelligence: sectorIntelligence,
    annexes: dm.annexes,
    branding: dm.branding,
  }, dm, 'executive_report');
}

