function templateId(dm) { return dm?.metadata?.template_id || dm?.template_id || 'unknown'; }
function title(dm) { return dm?.metadata?.template_name || dm?.title || 'VoiceInsights Intelligence Report'; }
function totalResponses(dm) { return Number(dm?.kpis?.total_responses || 0); }
function regionsCovered(dm) { return Number(dm?.kpis?.regions_covered || 0); }
function responseRate(dm) { return dm?.kpis?.response_rate_pct ?? null; }
function qualityScore(dm) { return dm?.report_quality_gate_v19?.overall_score ?? dm?.intelligence_os_v7?.report_quality_gate?.overall_score ?? null; }

function safeArray(value) { return Array.isArray(value) ? value : []; }
function percent(n, d) { return d > 0 ? Math.round((n / d) * 100) : 0; }

export function sanitizePublicReportTextV20(value) {
  if (Array.isArray(value)) return value.map(sanitizePublicReportTextV20);
  if (value && typeof value === 'object') {
    const out = {};
    for (const [k, v] of Object.entries(value)) out[k] = sanitizePublicReportTextV20(v);
    return out;
  }
  if (typeof value !== 'string') return value;
  let clean = value;
  for (const phrase of UNSAFE_PUBLIC_PHRASES) {
    clean = clean.replaceAll(phrase, PROFESSIONAL_FALLBACK);
  }
  return clean;
}

export function buildMethodologyTransparencyV20(dm) {
  const dq = dm.data_quality || {};
  const annex = dm.annexes || {};
  const questionnaire = safeArray(annex.questionnaire);
  const hasRawChannels = !!(dm.channel_breakdown || dm.channels || dm.metadata?.channels);
  return {
    title: 'Methodology Transparency',
    sample_size: totalResponses(dm),
    geography: {
      regions_covered: regionsCovered(dm),
      regions: safeArray(dm.demographics?.regions).map(r => ({ label: r.label, n: r.n, pct: percent(r.n, totalResponses(dm)) })),
    },
    channels: dm.channel_breakdown || dm.channels || ['Voice', 'WhatsApp', 'SMS', 'Web', 'Offline/App'].map(label => ({ label, note: hasRawChannels ? 'Reported channel' : 'Demonstration channel capability' })),
    respondent_profile: {
      gender: safeArray(dm.demographics?.gender),
      age: safeArray(dm.demographics?.age),
      note: 'Profile is reported from fields present in the report model; unavailable fields are not inferred.',
    },
    limitations: [
      'This demonstration report uses fictional sample data and must not be interpreted as real population evidence.',
      'Margin of error, design effects and weighting are only shown where the underlying report model supports them.',
      'Sector benchmarks are referenced only when present in the report model or standards library.',
    ],
    data_quality_score: qualityScore(dm),
    consent_coverage: dm.consent_coverage ?? dm.report_quality_gate_v19?.dimensions?.find(d => d.key === 'consent_coverage')?.value ?? 'Reported where available',
    confidence_level: dm.statistical_annex?.sample_statistics?.confidence_level || dm.confidence_level || 'Reported where available',
    evidence_type: dm.is_demo ? 'Synthetic demo evidence with report-model traceability' : 'Project evidence with available raw-source traceability',
    questionnaire_items: questionnaire.map((q, i) => ({ id: q.variable_id || `Q${i + 1}`, question_text: q.question_text || q.label || 'Question text unavailable', type: q.question_type || q.type || 'unknown' })),
  };
}

export function buildEvidenceTraceabilityV20(dm) {
  const base = buildEvidenceTraceabilityV19(dm);
  return base.map((e, idx) => {
    // Phase 20.1 production safety: a quote alone is not enough to call
    // evidence "raw-source" in the public/demo experience. Raw-source
    // evidence requires at least one auditable source pointer: response,
    // transcript, audio, consent, or an explicit raw evidence object.
    const hasRaw = !!(e.raw_response_evidence || e.audio_available || e.consent_available || e.transcript_available || e.response_id);
    const type = hasRaw ? 'raw-source evidence' : (dm.is_demo ? 'synthetic demo evidence' : 'report-model evidence');
    return {
      id: e.id || `E${idx + 1}`,
      claim: sanitizePublicReportTextV20(e.claim || e.finding || 'Evidence-backed claim'),
      evidence_classification: type,
      evidence_label: type,
      claim_to_source_path: [
        { level: 'claim', label: 'Claim', value: e.claim || e.finding || null },
        { level: 'finding', label: 'Finding', value: e.finding || dm.narrative?.key_findings?.[idx] || null },
        { level: 'indicator', label: 'Indicator', value: e.indicator || dm.findings?.topics?.[idx]?.topic || null },
        { level: 'chart', label: 'Chart / visual', value: e.chart_reference || dm.charts?.[idx]?.section || null },
        { level: 'question', label: 'Question', value: e.question_text || dm.annexes?.questionnaire?.[0]?.question_text || null },
        { level: 'source', label: hasRaw ? 'Raw source evidence' : type, value: hasRaw ? (e.response_id || 'Available auditable raw pointer') : 'Derived from report model / demonstration blueprint' },
      ],
      respondent_count: e.respondent_count || totalResponses(dm),
      region: e.region || dm.demographics?.regions?.[0]?.label || 'Multiple regions',
      confidence_score: e.confidence_score || qualityScore(dm) || 80,
      raw_available: hasRaw,
      quote: e.quote || null,
      response_id: e.response_id || null,
      transcript_available: !!e.transcript_available,
      audio_available: !!e.audio_available,
      consent_available: !!e.consent_available,
      public_disclosure: hasRaw
        ? 'This item includes an auditable raw-source pointer where available.'
        : 'This item is evidence derived from the report model or fictional demonstration dataset; it is not presented as raw transcript/audio evidence.',
    };
  });
}

function topSentiment(dm) {
  const total = totalResponses(dm);
  const pos = safeArray(dm.findings?.sentiment).find(s => /positive/i.test(s.label))?.n || 0;
  const neg = safeArray(dm.findings?.sentiment).find(s => /negative/i.test(s.label))?.n || 0;
  return { positive_pct: percent(pos, total), negative_pct: percent(neg, total) };
}

function buildRiskItems(dm) {
  const risks = safeArray(dm.narrative?.risks).slice(0, 3);
  const findings = safeArray(dm.narrative?.key_findings);
  return (risks.length ? risks : findings.slice(0, 3).map(f => `If this finding is not addressed, implementation effectiveness may be reduced: ${f}`)).map((risk, i) => ({
    risk: sanitizePublicReportTextV20(risk),
    likelihood: i === 0 ? 'High' : i === 1 ? 'Medium' : 'Medium',
    impact: i === 0 ? 'High' : i === 1 ? 'High' : 'Medium',
    priority: i === 0 ? 'Critical' : i === 1 ? 'High' : 'Medium',
  }));
}

function buildRecommendationItems(dm) {
  const recs = dm.recommendations || {};
  const rows = [
    ...safeArray(recs.immediate).map((r, i) => ({ recommendation: r, horizon: '0–30 days', owner: 'Field / Operations Team', priority: i === 0 ? 'High' : 'Medium' })),
    ...safeArray(recs.medium_term).map((r, i) => ({ recommendation: r, horizon: '3–6 months', owner: 'Programme Management', priority: i === 0 ? 'High' : 'Medium' })),
    ...safeArray(recs.long_term).map((r) => ({ recommendation: r, horizon: '6–12 months', owner: 'Country Leadership / Donor Liaison', priority: 'High' })),
  ];
  return rows.slice(0, 6).map((r, i) => ({
    ...r,
    recommendation: sanitizePublicReportTextV20(r.recommendation),
    expected_impact: i < 2 ? 'High management visibility and fast evidence of responsiveness.' : 'Structural improvement requiring follow-up measurement.',
    evidence_basis: 'Linked to report findings, recommendation tier and quality-gated report model.',
  }));
}

export function buildOnePageExecutiveBriefV20(dm) {
  const findings = safeArray(dm.narrative?.key_findings).slice(0, 5).map(sanitizePublicReportTextV20);
  const recommendations = buildRecommendationItems(dm).slice(0, 3);
  const risks = buildRiskItems(dm).slice(0, 3);
  const topFinding = findings[0] || PROFESSIONAL_FALLBACK;
  return {
    format: 'one_page_executive_brief',
    title: title(dm),
    headline_insight: sanitizePublicReportTextV20(dm.narrative?.executive_summary?.split('.')?.[0] || topFinding),
    executive_snapshot: {
      responses: totalResponses(dm),
      response_rate_pct: responseRate(dm),
      regions_covered: regionsCovered(dm),
      evidence_quality_score: qualityScore(dm),
      data_quality_score: qualityScore(dm),
      confidence_score: dm.ai_verification_v19?.overall_confidence || qualityScore(dm) || 80,
    },
    five_key_findings: findings,
    three_critical_risks: risks,
    three_recommended_decisions: recommendations.map(r => ({ decision: r.recommendation, owner: r.owner, timeline: r.horizon, priority: r.priority })),
    expected_impact: recommendations[0]?.expected_impact || 'Expected impact should be confirmed through a follow-up measurement round.',
    confidence_score: dm.ai_verification_v19?.overall_confidence || qualityScore(dm) || 80,
    evidence_quality: qualityScore(dm),
    decision_required: recommendations[0]?.recommendation || 'Approve the highest-priority recommendation and assign ownership for follow-up.',
  };
}

