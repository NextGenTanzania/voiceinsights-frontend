function applySiteLanguage(lang) {
  const dict = TRANSLATIONS[lang] || TRANSLATIONS.en;
  document.querySelectorAll('[data-i18n]').forEach(el => {
    const key = el.getAttribute('data-i18n');
    const text = dict[key] || TRANSLATIONS.en[key];
    if (text != null) el.innerHTML = text;
  });
  document.documentElement.lang = lang;
  localStorage.setItem('vi_app_lang', lang);
  document.querySelectorAll('#site-lang-switch button').forEach(b => b.classList.toggle('active', b.dataset.lang === lang));
}

function initSiteLanguage() {
  const saved = localStorage.getItem('vi_app_lang') || 'en';
  applySiteLanguage(saved);
  document.querySelectorAll('#site-lang-switch button').forEach(b => {
    b.addEventListener('click', () => applySiteLanguage(b.dataset.lang));
  });
}
