// FORMAT 9: DATASET APPENDIX — variable dictionary, question IDs,
// labels, metadata, missing values, survey structure.
// ------------------------------------------------------------
export function buildDatasetAppendixFormat(documentModel) {
  const dm = documentModel;
  const questions = dm.annexes?.questionnaire || [];

  // Variable dictionary derived directly from the real questionnaire
  // structure already stored in this report's annexes — never invented
  // variable names or labels not present in the actual survey instrument.
  const variableDictionary = questions.map((q, i) => ({
    variable_id: `Q${i + 1}`,
    order_index: q.order_index,
    label: q.question_text,
    type: q.question_type,
  }));

  return {
    format: 'dataset_appendix',
    title: dm.metadata.template_name,
    is_demo: !!dm.is_demo,
    survey_structure: {
      survey_title: dm.metadata.survey_title,
      total_questions: questions.length,
      channel_note: 'Data collected via mobile-based channels (SMS/WhatsApp/App/Web) — see Methodology for channel breakdown.',
    },
    variable_dictionary: variableDictionary,
    missing_values_note: 'Demographic fields (gender, age bracket, region) use an explicit "Not provided" category rather than blank/null values, so missingness is always visible rather than silently dropped from tables.',
    metadata: dm.metadata,
  };
}

// ------------------------------------------------------------
// FORMAT 10: AI EXECUTIVE TALKING POINTS — a 10-minute presentation's
// worth of speaking notes, expected questions, and suggested responses.
// ------------------------------------------------------------
// IMPORTANT: despite the name "AI", this is a DETERMINISTIC TEMPLATE
// assembly, not a new Claude call. Every talking point, every expected
// question, and every suggested response is built ONLY from narrative
// and recommendation text that already exists in this report's
// document_model_json. This is deliberate: Phase 15's core rule is that
// document_model_json is the ONLY source of truth and nothing should
// ever regenerate the report — an actual fresh AI call here would
// violate that rule and risk producing a talking point not grounded in
// the report's real evidence. Templating existing content guarantees
// "never fabricate" structurally, the same design principle used by
// every other format in this file.
// ------------------------------------------------------------
export function buildAiTalkingPointsFormat(documentModel) {
  const dm = documentModel;
  const narrative = dm.narrative || {};
  const recs = dm.recommendations || {};
  const sectorIntelligence = buildSectorIntelligence(dm);

  const talkingPoints = [
    narrative.executive_summary ? `Opening: ${narrative.executive_summary.split('.')[0]}.` : null,
    `This survey reached ${dm.kpis.total_responses} respondents across ${dm.kpis.regions_covered} regions, with a ${dm.kpis.response_rate_pct ?? '—'}% response rate.`,
    ...(narrative.key_findings || []).slice(0, 3).map(f => `Finding: ${f}`),
    (recs.immediate || [])[0] ? `Immediate priority: ${recs.immediate[0]}` : null,
    narrative.conclusions ? `Closing: ${narrative.conclusions.split('.')[0]}.` : null,
  ].filter(Boolean);

  // Expected questions are a FIXED set of the questions any stakeholder
  // audience typically asks, each answered using existing report content
  // — never inventing a new answer, and honestly saying so when the
  // report doesn't have the data to answer a typical question.
  const expectedQuestions = [
    {
      question: 'How confident are we in this data?',
      suggested_response: `${dm.kpis.total_responses} respondents were reached with a ${dm.kpis.response_rate_pct ?? '—'}% response rate. Average fraud-detection score was ${dm.data_quality?.avg_fraud_score ?? 'not available'}, with ${dm.data_quality?.flagged_response_count ?? 0} flagged responses.`,
    },
    {
      question: 'What is the single most important finding?',
      suggested_response: (narrative.key_findings || [])[0] || 'Insufficient verified evidence available to identify a leading finding.',
    },
    {
      question: 'What should we do first?',
      suggested_response: (recs.immediate || [])[0] || 'No immediate-tier recommendation is available yet for this report.',
    },
    {
      question: 'What are the biggest risks if we do nothing?',
      suggested_response: (narrative.risks || [])[0] || 'No specific risk has been identified in this report round.',
    },
    {
      question: 'How does this align with international standards?',
      suggested_response: (dm.metadata.standards || []).length ? `This report aligns with: ${dm.metadata.standards.join(', ')}.` : 'This report does not claim alignment with a specific international standard, consistent with its sector context.',
    },
  ];

  return {
    format: 'ai_talking_points',
    title: dm.metadata.template_name,
    is_demo: !!dm.is_demo,
    presentation_length: '10 minutes',
    sector_intelligence: sectorIntelligence,
    talking_points: talkingPoints,
    expected_questions: expectedQuestions,
    speaking_notes: 'Deliver the opening and KPI figures at a measured pace. Pause after each finding to allow questions. Close with the single conclusion sentence, then open the floor.',
    disclosure: 'This talking-points assembly is a deterministic template built ONLY from this report\'s existing content — it does not involve a new AI generation call, and cannot introduce any statistic, finding, or recommendation not already present in the report.',
  };
}


