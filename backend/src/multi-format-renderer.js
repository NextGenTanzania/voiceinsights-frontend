// FORMAT 5: DONOR BRIEF (4-8 pages) — UN Agencies, USAID, EU, World
// Bank, AfDB, FCDO.
// ------------------------------------------------------------
export function buildDonorBriefFormat(documentModel) {
  const dm = documentModel;
  const narrative = dm.narrative || {};
  const recs = dm.recommendations || {};
  const sectorIntelligence = buildSectorIntelligence(dm);

  // Value for Money is deliberately NOT an invented cost-benefit ratio —
  // it is a transparent, evidence-only statement of scale reached, since
  // this platform's document_model_json does not track programme cost
  // data. Fabricating a cost-per-outcome figure would violate the
  // never-invent-statistics rule; being explicit about this limitation
  // is the honest alternative.
  const valueForMoney = {
    scale_reached: `${dm.kpis.total_responses} respondents across ${dm.kpis.regions_covered} regions`,
    response_rate: dm.kpis.response_rate_pct,
    note: 'Cost-per-outcome figures require programme budget data not tracked in this survey platform and are intentionally not estimated here to avoid an unsupported financial claim.',
  };

  return withPhase20FormatPackage({
    format: 'donor_brief',
    title: dm.metadata.template_name,
    prepared_for: dm.metadata.organization_name,
    campaign: dm.metadata.campaign_name,
    generated_at: dm.metadata.generated_at,
    is_demo: !!dm.is_demo,
    results: {
      summary: narrative.executive_summary || 'Insufficient verified evidence available for this section.',
      key_findings: (narrative.key_findings || []).slice(0, 5),
    },
    impact: narrative.conclusions || 'Insufficient verified evidence available for this section.',
    value_for_money: valueForMoney,
    sector_intelligence: sectorIntelligence,
    donor_language_adaptation: sectorIntelligence.audience_adaptations?.donor || [],
    risks: narrative.risks || [],
    sustainability: (narrative.opportunities || []).filter(o => /sustain|continu|institutional|ownership/i.test(o)).concat(
      // if no opportunity explicitly mentions sustainability, fall back to
      // the long-term recommendations, which are the closest existing
      // evidence-based proxy for sustainability planning — never inventing
      // a new sustainability statement not grounded in the report.
      (narrative.opportunities || []).some(o => /sustain|continu|institutional|ownership/i.test(o)) ? [] : (recs.long_term || [])
    ),
    recommendations: recs,
    sdg_contribution: (dm.metadata.standards || []).filter(s => s === 'SDG' || s.startsWith('SDG')),
    all_standards: dm.metadata.standards || [],
    data_quality: dm.data_quality,
    branding: dm.branding,
  }, dm, 'donor_impact_report');
}

