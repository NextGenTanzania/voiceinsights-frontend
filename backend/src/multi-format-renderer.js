// FORMAT 8: STATISTICAL ANNEX (machine-readable) — all statistical
// tables, cross-tabs, indicators, metadata, confidence intervals.
// ------------------------------------------------------------
export function buildStatisticalAnnexFormat(documentModel) {
  const dm = documentModel;
  const n = dm.kpis.total_responses || 0;

  // A simple, transparent margin-of-error approximation (95% confidence,
  // standard proportion formula assuming p=0.5 for the conservative
  // widest-interval case) — a real statistical property of the sample
  // size itself, not an invented figure, and clearly labeled as an
  // approximation rather than a claim of precise survey-design-adjusted CI.
  const marginOfError = n > 0 ? Math.round((1.96 * Math.sqrt(0.25 / n)) * 1000) / 10 : null;

  return {
    format: 'statistical_annex',
    title: dm.metadata.template_name,
    generated_at: dm.metadata.generated_at,
    is_demo: !!dm.is_demo,
    sample_statistics: {
      total_responses: n,
      response_rate_pct: dm.kpis.response_rate_pct,
      regions_covered: dm.kpis.regions_covered,
      approximate_margin_of_error_pct: marginOfError,
      confidence_level: '95%',
      note: 'Margin of error is a standard statistical approximation based on sample size alone (conservative p=0.5 case) and does not account for design effects such as clustering or weighting.',
    },
    statistical_tables: dm.annexes?.statistical_tables || {},
    cross_tabs: {
      gender: dm.demographics?.gender || [],
      age: dm.demographics?.age || [],
      regions: dm.demographics?.regions || [],
      sentiment: dm.findings?.sentiment || [],
    },
    indicators: (dm.findings?.topics || []).map(t => ({ indicator: t.topic, frequency: t.count, pct_of_coded_mentions: dm.findings.topics.reduce((s, x) => s + x.count, 0) > 0 ? Math.round((t.count / dm.findings.topics.reduce((s, x) => s + x.count, 0)) * 1000) / 10 : null })),
    data_quality: dm.data_quality,
    metadata: dm.metadata,
  };
}

