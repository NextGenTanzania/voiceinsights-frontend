// Every function here is a PURE, DETERMINISTIC transformation of an
// ALREADY-EXISTING document_model_json — none of them call Claude, none
// of them touch the database, none of them can ever produce a statistic,
// finding, or recommendation not already present in the source report.
// This is deliberate: "never fabricate" is trivially guaranteed when a
// format is nothing more than a reorganized SUBSET or RELABELING of
// content that has already passed through the real Report Engine and
// (for Flagship Showcase reports) is permanently locked.
//
// One report -> many professional outputs, with zero duplicate
// processing and zero risk of drifting from the source of truth.
// ============================================================

// A simple, transparent, fully-explainable risk rating derived ONLY from
// data already present in the report (negative sentiment share and
// fraud-flag rate) — never an AI judgment call, so it can never invent a
// rating unsupported by the report's own numbers.
function computeRiskRating(documentModel) {
  const total = documentModel.kpis?.total_responses || 0;
  if (total === 0) return { rating: 'Not yet assessable', basis: 'No responses collected yet.' };
  const negativeEntry = (documentModel.findings?.sentiment || []).find(s => s.label === 'negative');
  const negativePct = negativeEntry ? Math.round((negativeEntry.n / total) * 100) : 0;
  const flaggedPct = total > 0 ? Math.round(((documentModel.data_quality?.flagged_response_count || 0) / total) * 100) : 0;
  let rating = 'Standard';
  if (negativePct >= 25 || flaggedPct >= 10) rating = 'Elevated';
  if (negativePct >= 40 || flaggedPct >= 20) rating = 'High';
  return { rating, basis: `${negativePct}% negative sentiment, ${flaggedPct}% flagged responses (data-quality basis).` };
}

