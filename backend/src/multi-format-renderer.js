// FORMAT 6: POLICY BRIEF (4-8 pages) — Government standard, for
// Ministries, Commissioners, Policy Makers.
// ------------------------------------------------------------
export function buildPolicyBriefFormat(documentModel) {
  const dm = documentModel;
  const narrative = dm.narrative || {};
  const recs = dm.recommendations || {};
  const sectorIntelligence = buildSectorIntelligence(dm);

  // "Problem" is framed from the single most-cited finding (the report's
  // own topic-frequency data), not a fresh AI judgment about what the
  // problem is — this keeps the framing strictly evidence-derived.
  const topTopic = (dm.findings?.topics || [])[0];
  const problem = topTopic
    ? `The most frequently raised concern in this evidence base is "${topTopic.topic}" (${topTopic.count} mentions), indicating this is the population's leading unmet need.`
    : 'Insufficient thematic data has been collected yet to identify a leading policy-relevant concern.';

  // "Options" reuses the existing recommendation tiers, reframed as policy
  // options with an escalating scope (operational -> policy) rather than
  // inventing separate policy options not grounded in the same evidence.
  const options = [
    ...(recs.immediate || []).map(a => ({ option: a, scope: 'Operational — no new policy required' })),
    ...(recs.medium_term || []).map(a => ({ option: a, scope: 'Programme design — may require budget reallocation' })),
    ...(recs.long_term || []).map(a => ({ option: a, scope: 'Policy-level — may require new regulation or budget line' })),
  ];

  return withPhase20FormatPackage({
    format: 'policy_brief',
    title: dm.metadata.template_name,
    prepared_for: dm.metadata.organization_name,
    campaign: dm.metadata.campaign_name,
    generated_at: dm.metadata.generated_at,
    is_demo: !!dm.is_demo,
    problem,
    evidence: {
      total_responses: dm.kpis.total_responses,
      regions_covered: dm.kpis.regions_covered,
      key_findings: (narrative.key_findings || []).slice(0, 5),
      top_topics: (dm.findings?.topics || []).slice(0, 5),
    },
    policy_implications: narrative.discussion || 'Insufficient verified evidence available for this section.',
    sector_intelligence: sectorIntelligence,
    government_language_adaptation: sectorIntelligence.audience_adaptations?.government || [],
    options,
    recommendations: recs,
    expected_outcomes: narrative.opportunities || [],
    standards: dm.metadata.standards || [],
    branding: dm.branding,
  }, dm, 'government_report');
}

