    five_key_findings: (narrative.key_findings || []).slice(0, 5),
    five_priority_recommendations: fivePriorityRecommendations,
    sdg_alignment: (dm.metadata.standards || []).filter(s => s === 'SDG' || s.startsWith('SDG')),
